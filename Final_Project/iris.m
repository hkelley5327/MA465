% (b) Chi-square goodness of fit test

sigmas = [0.1, 0.3];

% Function to compute chi-square
chi2_func = @(y, X, a, sigma) sum(((y - X*a).^2) / sigma^2);

% Degrees of freedom (same as before)
df_setosa = size(Iris_setosa,1) - 4;
df_versicolor = size(Iris_versicolor,1) - 4;
df_virginica = size(Iris_virginica,1) - 4;

% Loop over sigma values
for s = 1:length(sigmas)
    sigma = sigmas(s);

    % --- Setosa ---
    y = Iris_setosa(:,1);
    X = [ones(size(Iris_setosa,1),1) Iris_setosa(:,2:4)];
    chi2_setosa = chi2_func(y, X, a_setosa, sigma);

    % --- Versicolor ---
    y = Iris_versicolor(:,1);
    X = [ones(size(Iris_versicolor,1),1) Iris_versicolor(:,2:4)];
    chi2_versicolor = chi2_func(y, X, a_versicolor, sigma);

    % --- Virginica ---
    y = Iris_virginica(:,1);
    X = [ones(size(Iris_virginica,1),1) Iris_virginica(:,2:4)];
    chi2_virginica = chi2_func(y, X, a_virginica, sigma);

    % --- Critical chi-square (99%) ---
    chi2crit_setosa = chi2inv(0.99, df_setosa);
    chi2crit_versicolor = chi2inv(0.99, df_versicolor);
    chi2crit_virginica = chi2inv(0.99, df_virginica);

    % --- Accept/reject ---
    acc_setosa = chi2_setosa < chi2crit_setosa;
    acc_versicolor = chi2_versicolor < chi2crit_versicolor;
    acc_virginica = chi2_virginica < chi2crit_virginica;

    % --- Display ---
    fprintf('\n===== Sigma = %.1f =====\n', sigma);

    fprintf('Setosa:     chi2 = %.2f, crit = %.2f, acceptable = %d\n', ...
        chi2_setosa, chi2crit_setosa, acc_setosa);

    fprintf('Versicolor: chi2 = %.2f, crit = %.2f, acceptable = %d\n', ...
        chi2_versicolor, chi2crit_versicolor, acc_versicolor);

    fprintf('Virginica:  chi2 = %.2f, crit = %.2f, acceptable = %d\n', ...
        chi2_virginica, chi2crit_virginica, acc_virginica);
end

% Part 3 -------------------------------------------------------------------
% (a) Simple linear regression: y = a + b*x

figure;

species_names = {'Setosa','Versicolor','Virginica'};
datasets = {Iris_setosa, Iris_versicolor, Iris_virginica};

for k = 1:3

    data = datasets{k};

    x = data(:,3); % Petal Length
    y = data(:,1); % Sepal Length

    % Design matrix [1 x]
    X = [ones(length(x),1) x];

    % Coefficients
    a = X \ y;

    % Residuals
    r = y - X*a;

    N = length(y);
    p = 2;

    % Variance estimate
    sigma2 = sum(r.^2)/(N - p);

    % Covariance matrix
    CovA = sigma2 * inv(X' * X);

    % Standard errors
    se = sqrt(diag(CovA));

    fprintf('\n%s:\n', species_names{k});
    fprintf('Intercept (a) = %.4f ± %.4f\n', a(1), se(1));
    fprintf('Slope (b)     = %.4f ± %.4f\n', a(2), se(2));

    % Plot
    subplot(1,3,k);
    scatter(x,y,'filled'); hold on;

    x_fit = linspace(min(x), max(x), 100);
    y_fit = a(1) + a(2)*x_fit;

    plot(x_fit, y_fit, 'r', 'LineWidth', 2);

    title(species_names{k});
    xlabel('Petal Length');
    ylabel('Sepal Length');
    grid on;

    % --- Correlation coefficient ---
    r_corr = corr(x,y);
    
    % --- t-test for correlation ---
    t_stat = r_corr * sqrt((N - 2)/(1 - r_corr^2));
    t_crit = tinv(0.975, N - 2);
    
    significant = abs(t_stat) > t_crit;
    
    fprintf('Correlation r = %.4f\n', r_corr);
    fprintf('t = %.4f, t_crit = %.4f, significant (95%%) = %d\n', t_stat, t_crit, significant);
end

% Functions ---------------------------------------------------------------------------------
function [a, se] = linreg_with_errors(X, y)

    % Fit coefficients
    a = X \ y;

    % Residuals
    r = y - X*a;

    N = length(y);
    p = size(X,2);

    % Estimate variance of noise
    sigma2 = sum(r.^2) / (N - p);

    % Covariance matrix of parameters
    CovA = sigma2 * inv(X' * X);

    % Standard errors
    se = sqrt(diag(CovA));

end