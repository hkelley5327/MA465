% Virginica
y = Iris_virginica(:,1);
X = [ones(size(Iris_virginica,1),1) Iris_virginica(:,2:4)];
[a_virginica, se_virginica] = linreg_with_errors(X, y)

% Part 2 ---------------------------------------------------------------------------
% (a) t-statistics + 99% significance test

% Degrees of freedom
df_setosa = size(Iris_setosa,1) - 4;
df_versicolor = size(Iris_versicolor,1) - 4;
df_virginica = size(Iris_virginica,1) - 4;

% t-statistics
t_setosa = a_setosa ./ se_setosa;
t_versicolor = a_versicolor ./ se_versicolor;
t_virginica = a_virginica ./ se_virginica;

% Critical t-values (two-tailed, 99% confidence)
tcrit_setosa = tinv(0.995, df_setosa)
tcrit_versicolor = tinv(0.995, df_versicolor)
tcrit_virginica = tinv(0.995, df_virginica)

% Significance masks (true = needed regressor)
sig_setosa = abs(t_setosa) > tcrit_setosa;
sig_versicolor = abs(t_versicolor) > tcrit_versicolor;
sig_virginica = abs(t_virginica) > tcrit_virginica;

% Display results in readable format
labels = ["Intercept", "Sepal Width", "Petal Length", "Petal Width"];

